#!/usr/bin/perl

my $filename = "/usr/share/dict/words";

sub search 
{
	print "\nIncorrect words  :  \n\n";
	my $count = 0;

	foreach $elem (@_)
	{
		if ($elem ne '')
		{
			$elem1 = lc $elem;
			my $word = `grep -w $elem1 $filename`;
			if ($word eq "")
			{
				print "$elem\n";
				$count++;
			}
		}
	}
	print "\nTotal: $count\n\n";
}
print "*********************************************************************************************************************************";
print "\n---------------------------------------------------Welcome to Spell Checker------------------------------------------------------\n";
print "*********************************************************************************************************************************\n";
print "Enter a string : \n";
my $string = <STDIN>;
chomp ($string);
my @arr = split (/[\W]/, $string);
search (@arr);
print "*********************************************************************************************************************************";
print "\n---------------------------------------------------Thank you for using spellchecker----------------------------------------------\n";
print "*********************************************************************************************************************************\n";